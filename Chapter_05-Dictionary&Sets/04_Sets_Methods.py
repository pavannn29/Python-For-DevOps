# we will see a list of all the functions provided by Python to deal with Sets.

# Creating a set

my_set = {10, 24, 29, 8, 12, 27, 34}

# 1. add(): Adds a given element to a set

my_set.add(56)
print("Set after adding: ", my_set)

# 2. remove(): Removes the element from the set

my_set.remove(8)
print("Set after removing: ", my_set)

# 3. clear(): Removes all elements from the set

my_set.clear()
print("Set after clearing: ", my_set)