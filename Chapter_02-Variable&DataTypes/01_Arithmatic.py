''' 
Arithmatic operators are used to perform basic mathematical operations like addition, 
Substraction, Division and multiplication. 
'''

a = 24
b = 8

# Addition 
print("Addition: ", a + b)

# Subtraction
print("Subtraction: ", a - b)

# Division
print("Division: ", a / b)

# Multiplication
print("Multiplication:", a * b)  

# Floor Division
print("Floor Division:", a // b)  

# Modulus
print("Modulus:", a % b) 

# Exponentiation
print("Exponentiation:", a ** b)

